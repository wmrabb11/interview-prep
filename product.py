
def product(num1, num2):
	place = 1
	value_of_num1 = 0
	for x in xrange(len(num1) - 1, -1, -1):
		digit = int(num1[x])
		value = digit * place
		place *= 10
		value_of_num1 += value

	place_2 = 1
	value_of_num2 = 0
	for x in xrange(len(num2) - 1, -1, -1):
		digit = int(num2[x])
		value = digit * place_2
		place_2 *= 10
		value_of_num2 += value
	return value_of_num1 * value_of_num2

